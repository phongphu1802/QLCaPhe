    private void lbl_SuaNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbl_SuaNhanVienMouseClicked
        int input=JOptionPane.showConfirmDialog(null, "Bạn muốn sửa nhân viên ?","Xác nhận",JOptionPane.YES_NO_OPTION);
        // 0=yes, 1=no
        if(input == 0){
            Date day= new Date();
            //Format ngay ve dang yyyy-MM-dd
            DateFormat df=new SimpleDateFormat("yyyy-MM-dd");
            //Test
            //System.out.print("Nhân viên: "+txt_MaNhanVien.getText()+"/ "+txt_HoNhanVien.getText()+"/ "+txt_TenNhanVien.getText()+"/ "+cbb_GioiTinh.getSelectedItem()+"/ "+df.format(dc_NgaySinh.getDate())+"/ "+txt_DiaChi.getText()+"/ "+txt_CMND.getText()+"/ "+txt_SoDienThoai.getText()+"/ "+java.time.LocalDate.now()+"/ "+cbb_ChucVu.getSelectedItem()+"/ 0");
            Vector row=new Vector();
            row.add(txt_MaNhanVien.getText());
            row.add(txt_HoNhanVien.getText());
            row.add(txt_TenNhanVien.getText());
            row.add(cbb_GioiTinh.getSelectedItem());
            row.add(df.format(dc_NgaySinh.getDate()));
            row.add(txt_DiaChi.getText());
            row.add(txt_CMND.getText());
            row.add(txt_SoDienThoai.getText());
            row.add(java.time.LocalDate.now());
            for(ChucVuDTO cv: arCV){
                //Kiểm tra để lấy mã chức vụ
                if(cv.getStrTenCV().equals(cbb_ChucVu.getSelectedItem())){
                    System.out.print(cv.getStrMaCV());
                    row.add(cv.getStrMaCV());
                }
            }
            row.add(1);
            try {
                NhanVien.CapNhatNhanVien(row);
            } catch (Exception ex) {
                Logger.getLogger(FrameThemNhanVien.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_lbl_SuaNhanVienMouseClicked
    
