    public void setChucVu() throws Exception{
        arCV=ChucVu.LoadChucVu();
        for(ChucVuDTO cv:arCV){
            //System.out.println(cv.getStrMaCV());
            //Kiểm tra nếu là Admin thì không thêm vào
            if(cv.getStrMaCV().equals("AD")){ 
            }else{
                cbb_ChucVu.addItem(cv.getStrTenCV());
            }
        }
    }
    
    //SET THÔNG TIN NHÂN VIÊN CẦN SỬA VÀO
    public void setThongTin(String strMaNV) throws Exception{
        arNV.clear();
        arNV=NhanVien.SelectNhanVien(strMaNV);
        //Set dữ liệu vào txt
        txt_MaNhanVien.setText(arNV.get(0).getStrMaNV());
        txt_HoNhanVien.setText(arNV.get(0).getStrHoNV());
        txt_TenNhanVien.setText(arNV.get(0).getStrTenNV());
        cbb_GioiTinh.setSelectedItem(arNV.get(0).getStrGioiTinh());
        //Format string thành Date với dạng yyyy-MM-dd
        Date date = new SimpleDateFormat("yyyy-MM-dd").parse(arNV.get(0).getStrNgaySinh());
        dc_NgaySinh.setDate(date);
        txt_DiaChi.setText(arNV.get(0).getStrDiaChi());
        txt_CMND.setText(arNV.get(0).getStrCMND());
        txt_SoDienThoai.setText(arNV.get(0).getStrSDT());
        for(ChucVuDTO cv:arCV){
            if(arNV.get(0).getStrChucVu().equals(cv.getStrMaCV()))
            cbb_ChucVu.setSelectedItem(cv.getStrTenCV());
        }
    }
    
