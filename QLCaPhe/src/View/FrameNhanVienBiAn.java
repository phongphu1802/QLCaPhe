    private void lbl_MoAnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbl_MoAnMouseClicked
        int iSelect=tbl_DSNhanVienAn.getSelectedRow();
        //Kiểm tra có chọn Row trong table không
        if(iSelect>=0){
            int input=JOptionPane.showConfirmDialog(null, "Bạn muốn mở ẩn nhân viên ?","Xác nhận",JOptionPane.YES_NO_OPTION);
            // 0=yes, 1=no
            if(input==0){
                    try {    
                        NhanVien.CapNhatTrangThai((String) model.getValueAt(iSelect, 1),"1");
                        model.removeRow(iSelect);
                        model.fireTableDataChanged();
                    } catch (Exception ex) {
                        Logger.getLogger(FrameNhanVien.class.getName()).log(Level.SEVERE, null, ex);
                    }
                
            }
        }else{
            JOptionPane.showMessageDialog(null,"Bạn chưa chọn Row.");
        }
    }//GEN-LAST:event_lbl_MoAnMouseClicked

